package com.garin.models;

public class Movie {
    public int id;
    public String title;
    public String imgPath;
    public int age;
    public String describe;

    public int getId(){
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getImgPath() {
        return imgPath;
    }

    public int getAge() {
        return age;
    }

    public String getDescribe() {
        return describe;
    }
}
